    public void seatColourChanger(JButton seat) {
        String currentSeatColour = seat.getBackground().toString();
        //if the current seat colour equals blue (available) && seatCounter is less than 7
        if (seatCounter > 5) {
            MaxSeatsLabel.setVisible(true);

            if (currentSeatColour.equals("java.awt.Color[r=51,g=255,b=51]")) {
                //if the current colour is green, set colour to blue
                seat.setBackground(new Color(0, 153, 255));                
                MaxSeatsLabel.setVisible(false);                
                String seatName = seat.getText();// seat number used to match with ticket
                
                for (int i = 0; i < selectedTickets.size(); i++) {
                    if (seatName.equals(selectedTickets.get(i).getSeat())) {
                        selectedTickets.remove(i);
                    }                    
                }
                
                fillTicketTable(); //updating ticket table to show user all tickets selected                        
                seatCounter--; //minus one from seatCounter
            }
        } else {
            MaxSeatsLabel.setVisible(false);
            if (currentSeatColour.equals("java.awt.Color[r=0,g=153,b=255]")) {
                NotEnoughTicketError.setVisible(false);
                //set colour of seat to green to signify the user has chosen that seat
                seat.setBackground(new Color(51, 255, 51));

                seatCounter++; //add one to seatCounter

                //create ticket object of that seat
                int ticketID = databaseSQL.getMaxTicketID() + selectedTickets.size();               
                String seatNO = seat.getText();
                
                //default age:
                String age = "Adult";
                
                //ticket price:
                int price = priceCalculator(seatNO, age);
                System.out.println("\nPrice of ticket: " + price);
      
                //create new object of ticket to be added to booking tickets arraylist
                ticket newTicket = new ticket(ticketID, databaseSQL.getCurrentUser().getAccountID(), UpcomingEvents.getChosenEvent().getEventID(), BuyTickets.selectedStand, seatNO, price, age);

                selectedTickets.add(newTicket);

                fillTicketTable(); //updates tickets to table to show user  

            } else if (currentSeatColour.equals("java.awt.Color[r=51,g=255,b=51]")) {

                //if the current colour is green, set colour to blue
                seat.setBackground(new Color(0, 153, 255));

                //remove ticket from list
                String seatName = seat.getText();// seat number used to match with ticket
                for (int i = 0; i < selectedTickets.size(); i++) {
                    if (seatName.equals(selectedTickets.get(i).getSeat())) {
                        selectedTickets.remove(i);
                    }
                }
                fillTicketTable(); //updates tickets to table to show user

                seatCounter--; //minus one from seatCounter
            }
        }
        System.out.println("Seat count: " + seatCounter);
        
    }
    
    public static int priceCalculator(String seat, String age) {
        int band = UpcomingEvents.getChosenEvent().getEventBand();
        int ageNumber = 3;
        int seatNumber = Integer.parseInt(seat);
        int distanceCost = 10;
        
        
        //get ageNumber multiplyer
        if (age.equals("Adult")) {
            ageNumber = 3;
        } else if (age.equals("Senior") || age.equals("Student")) {
            ageNumber = 2;
        } else if (age.equals("Child")) {
            ageNumber = 1;
        }
        
        //get seat distance
        if (seatNumber <= 20) {
            distanceCost = 30;
        } else if (seatNumber >= 21 && seatNumber <= 60) {
            distanceCost = 20;
        } else if (seatNumber >= 61 && seatNumber <= 100) {
            distanceCost = 10;
        }

        int newPrice = (band * ageNumber) + 25 + distanceCost;        
//        System.out.println("\nPrice of ticket: " + newPrice);
        return newPrice;
    }
    
