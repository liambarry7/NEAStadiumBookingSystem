    public void fillTicketTable() {
        DefaultTableModel tblModel = (DefaultTableModel)TicketsTable.getModel();
        
        //removes previous rows in table for complete refresh and prevention of duplicates
        tblModel.setRowCount(0);       
        
        //load tickets into the table
        for (int i = 0; i < selectedTickets.size(); i++) {
            String stand = selectedTickets.get(i).getStand();
            String seat = selectedTickets.get(i).getSeat();
            String age = selectedTickets.get(i).getAge();
            int priceHolder = selectedTickets.get(i).getPrice();
            String price = Integer.toString(priceHolder);
            
            String data[] = {stand, seat, age, price};
            tblModel.addRow(data);
        }
        getTotalPrice(); //refresh total price after every change
    }
   
    public void fillBookedSeats(JButton seatButton) {
        //interact with database and get all booked tickets for that stand and the seats that are booked
        String standName = StandLabel.getText().toString();
        ArrayList<ticket> unavailableSeats = databaseSQL.getEventBookedTickets(UpcomingEvents.getChosenEvent().getEventID(), standName);
        ArrayList<seasonTicket> seasonTicketSeats = databaseSQL.getAllST();
        
        //loop through every seat and make red if needed

        //get seat number and turn seat to red and unclickable
        for (int i = 0; i < unavailableSeats.size(); i++) {

            //get seat number of booked seat
            String seatNumber = unavailableSeats.get(i).getSeat();
            
            if (seatNumber.equals(seatButton.getText())) {
                seatButton.setBackground(new Color(255,0,51));
                seatButton.setEnabled(false); //button cannot be clicked
            }
        }
//////////////////////////////////////////////////////////////////////////////////////////////////////        
        for (int i = 0; i < seasonTicketSeats.size(); i++) {
            
            //get seat number of booked seat
            String seatNumber = seasonTicketSeats.get(i).getSeat();
            String standNameST = seasonTicketSeats.get(i).getStand();
            
            if (seatNumber.equals(seatButton.getText()) && standNameST.equals(standName)) {
                seatButton.setEnabled(false);
                seatButton.setBackground(new Color(255,0,51));                
            }
        }
    }
    
