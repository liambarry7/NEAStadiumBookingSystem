    private void ApplyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ApplyButtonActionPerformed
        try {
            //get age user wants to change ticket to
            String chosenAge = AgeSelector.getSelectedItem().toString();
            
            //match seats as seats are unique
            String currentSeat = (String) TicketsTable.getValueAt(TicketsTable.getSelectedRow(), 1); //gets value of object at selected row, seat column and casts it to be a string
//            System.out.println(currentSeat);
            
            for (int i = 0; i < selectedTickets.size(); i++) {
                String searchTicketSeat = selectedTickets.get(i).getSeat(); //looking for this ID
                if (searchTicketSeat.equals(currentSeat)) {
                    selectedTickets.get(i).setAge(chosenAge);
                    int newPrice = priceCalculator(currentSeat, chosenAge);
                    System.out.println("\nNew price of ticket: " + newPrice);
                    selectedTickets.get(i).setPrice(newPrice);
                }

            }
            
            fillTicketTable();
            
        } catch (Exception e) {
            System.out.println(" ");
        }

    }//GEN-LAST:event_ApplyButtonActionPerformed

